      SUBROUTINE ZEBRA_INLINE_DECOMPRESS(LUN, LENGTH, FILENAME)
      IMPLICIT NONE

      INTEGER LUN
      INTEGER LENGTH, LENMAX
      CHARACTER*1 FILENAME(LENGTH)

      EXTERNAL READ_COMPRESSED  ! ROUTINE WHICH HANDLES DECOMPRESSION

      LENMAX = LENGTH
      IF(LENMAX.GT.64)LENMAX = 64

C      WRITE(6,1)LUN,(FILENAME(I),I=1,LENMAX)
C 1    FORMAT(/,' ZEBRA_INLINE_COMPRESS <I>: ',/,
C     +         ' DOING IN-LINE DECOMPRESSION FROM UNIT',I4,
C     +       /,' FOR FILE NAME: ',64A1)

      CALL FZFILE(LUN, 9990, 'XIC')
      CALL FZHOOK(LUN, READ_COMPRESSED, 0)

      RETURN
      END
C
C     DECOMPRESSION INTERFACE
C
      SUBROUTINE READ_COMPRESSED(IBUF, IOWAY)
      IMPLICIT NONE

      INTEGER IBUF(*), IOWAY, NW, IQUEST
      COMMON /QUEST/ IQUEST(100)

      CALL COMPREAD(IBUF, IQUEST, NW)

      RETURN
      END
